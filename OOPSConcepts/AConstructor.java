package OOPSConcepts;

public class AConstructor {
	
	public AConstructor() {
		System.out.println("A class Parent Constructor");
	}
	
	public AConstructor(int i) {
		System.out.println("Parent class Constructor with 1 parameter" +i);
	}
	
	public AConstructor(int i,int j) {
		System.out.println("Parent class Constructor with 2 parameter " +i);
		System.out.println("Parent class Constructor with 2 parameter " +j);
	}


}
